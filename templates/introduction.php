<h1>握手302</h1>
<p>“握手302”是一间位于白石洲上白石村二坊49栋302号的出租屋。城中村里有着惊人的建筑密度，人们把这些以出租为目的的村民自建房屋戏称为“握手楼”。这间12.5平米的小房间位于本栋 “握手楼”的第三层，包括一个房间和一个洗手间，这样的房间一层共有十间。</p>
<p>这是一个独立的艺术空间，有一个5人的核心团队负责策划和日常工作，但通过公开的通知，任何人都能参与到这里的艺术实践和交流里来。项目的参与者们会在这里工作甚至居住。从2013年10月开始至今，握手302已经在九个不同的主题之下举办了超过五十次系列活动。</p>
<p>我们相信：艺术属于每一个为我们城市做出贡献的人</p>
